    end
    if neon0 == "Activé" and neon1 == "Activé" and neon2 == "Activé" and neon3 == "Activé" then
        neon4 = 1
    else
        neon4 = 0
    end
    Vehicledata = {
        ["Colours"]=table.pack(GetVehicleColours(currentvehicle)),
        ["ExtraColours"]=table.pack(GetVehicleExtraColours(currentvehicle)),
