    else
        neon1 = "Désactivé"
    end
    if IsVehicleNeonLightEnabled(currentvehicle,2) then
        neon2 = "Activé"
    else
        neon2 = "Désactivé"
    end
    if IsVehicleNeonLightEnabled(currentvehicle,3) then
        neon3 = "Activé"
    else
        neon3 = "Désactivé"
    end
    if GetVehicleTyresCanBurst(currentvehicle) then
